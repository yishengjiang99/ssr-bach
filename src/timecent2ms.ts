const lgl = timecent2ms.length;
export function tc2ms(timecent: TimeCent) {
  let ms = 0;
  if (timecent < 0)
    while (timecent > -1 * timecent2ms[ms]) {
      ms++;
    }
  else while (timecent > timecent2ms[lgl - ms - 1]) ms++;
  return ms;
}
